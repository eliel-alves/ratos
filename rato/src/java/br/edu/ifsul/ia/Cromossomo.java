package br.edu.ifsul.ia;


public class Cromossomo {
	
	Integer r, g, b;

	Cromossomo(Integer r, Integer g, Integer b) {
		super();
		this.r = r;
		this.g = g;
		this.b = b;
	}

	public Integer getR() {
		return r;
	}

	public void setR(int r) {
		this.r = r;
	}

	public Integer getG() {
		return g;
	}

	public void setG(int g) {
		this.g = g;
	}

	public Integer getB() {
		return b;
	}

	public void setB(int b) {
		this.b = b;
	}
	
}
	
	
